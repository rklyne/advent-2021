BRACES = {
        '{': '}',
        '(': ')',
        '<': '>',
        '[': ']',
        }
REVERSE = {v:k for (k,v) in BRACES.items()}
SCORE = {
    ')': 3,
    ']': 57,
    '}': 1197,
    '>': 25137,
    }

def line_score(line):
  stack = []
  for char in line:
    if char in BRACES:
      stack.append(char)
      continue
    try:
      must_match = stack.pop()
    except:
      must_match = None
    if REVERSE[char] != must_match:
      return ['corrupt', SCORE[char]]
  if stack:
    return ['incomplete', stack]
  return ['good', 0]

def part1():
  total = 0
  for line in filter(bool, text.split("\n")):
    result, number = line_score(line)
    if result == 'corrupt':
      total += number
  return total

def part2():
  allscores = []
  scores = {'(': 1, '[': 2, '{': 3, '<': 4}
  for line in filter(bool, text.split("\n")):
    result, stack = line_score(line)
    if result == 'incomplete':
      score = 0
      for char in reversed(stack):
        score *= 5
        score += scores[char]
      allscores.append(score)


  result = list(sorted(allscores))
  while len(result) > 2:
    result = result[1:-1]
  return result[0]

print(part1())
print(part2())
